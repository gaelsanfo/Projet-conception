
public class MaDate {
	
	public MaDate() {
		super();
	}

	public MaDate(int jour, int mois, int annee) {
		super();
		this.jour = jour;
		this.mois = mois;
		this.annee = annee;
	}

	private int jour, mois, annee;

	public int getJour() {
		return jour;
	}

	public void setJour(int jour) {
		this.jour = jour;
	}

	public int getMois() {
		return mois;
	}

	public void setMois(int mois) {
		this.mois = mois;
	}

	public int getAnnee() {
		return annee;
	}

	public void setAnnee(int annee) {
		this.annee = annee;
	}
	
	public String afficheDate() {
		return jour+"/"+mois+"/"+annee;
	}
	
	
	
}
